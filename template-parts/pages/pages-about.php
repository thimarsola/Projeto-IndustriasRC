                    <p>Com uma base sólida e projetando o futuro, as Indústrias RC se viram prontas para novos saltos.</p>
                    <br>
                    <p>Foi quando em 2007, nasceu a Industrial Tecno-Fix, localizada na Capital de São Paulo, e especializada na fabricação de peças usinadas e estampadas, de acordo com a necessidade do cliente. O sucesso da Industrial Tecno-Fix foi perceptível, e em curto espaço de tempo ela se estruturou com equipamentos modernos de ponta para o segmento de usinagem e estamparia.</p>
                    <br>
                    <p>No ano de 2014, a Industrial Tecno-fix se mudou para a cidade de Diadema – SP, ocupando um galpão de 1200 metros de área fabril.</p>
                    <br>
                    <p>Sempre em busca de novos mercados, as Indústrias RC adquiriram em maio de 2014 a empresa KSB Borrachas (fabricante de coxins de escapamento e diversos), e a partir daí se iniciou um trabalho de desenvolvimento de novos itens em metal-borracha para o segmento de reposição automotiva.</p>
                    <br>
                    <p>No ano de 2017, objetivando alcançar novos segmentos de mercado, as Indústrias RC criaram a empresa BBTEC, fabricante de barramentos blindados (também conhecidos como busway) e trilhos eletrificados (também conhecidos como mini-way), abrangendo assim sua atuação para o setor elétrico.</p>
                    <br>
                    <p>No ano de 2021, as empresas das Indústrias RC alcançaram a marca de 100 colaboradores, contando com mais de 100 máquinas operatrizes, entre elas: tornos automáticos monofuso e multifuso, tornos e fresadoras CNC, prensas, retíficas, furadeiras, rosqueadeiras, máquinas especiais, entre outras, que juntas têm capacidade de transformação de mais de 100 toneladas de aço por mês.</p>
                    <br>
                    <p>Em meio a esse marco, em Agosto de 2022, os sócios decidiram mudar a razão social da Industrial Tecno-fix para Indústrias RC Ltda, e ainda em Outubro de 2022 o Grupo IRC decidiu alterar sua marca da linha de metal-borracha, tornando a KSB Borrachas a chamar-se IRC Automotiva.</p>
