                    <p>As Indústrias RC nasceram em 2001 quando um dos fundadores, já atuante no mercado desde 1989, e seu irmão, decidiram fundar a primeira empresa do Grupo Empresarial: a Abratec, e situá-la em Belo Horizonte, Minas Gerais. A Abratec foi criada com o objetivo de suprir a demanda de peças de fixação especiais, em razão do forte crescimento do mercado mineiro.</p>
